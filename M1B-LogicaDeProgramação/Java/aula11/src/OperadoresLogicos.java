public class OperadoresLogicos {

    public static void main(String [] args){

        boolean a = true;
        boolean b = false;

        // Operador && (AND)
        System.out.println("a && b: " + (a && b));

        // Operador || (OR)
        System.out.println("a || b: " + (a || b));

        // Operador ! (NOT)
        System.out.println("!a: " + (!a));
        System.out.println("!b: " + (!b));

    }

}
